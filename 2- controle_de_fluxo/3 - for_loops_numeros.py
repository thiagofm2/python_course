"""
Veremos sobre os Loops For, que são um dos métodos mais importantes na programação em python, pois ele itera, ou seja
passa diversas vezes por um determinado número de dados, até obter um resultado específico que desejarmos.

Vamos ver como ele funciona com números
"""
for number in range(1, 6):         # Com esse código, será printado na tela de 1 até 5.
    print(number)
